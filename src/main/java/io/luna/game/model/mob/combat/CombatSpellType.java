package io.luna.game.model.mob.combat;

/**
 * An enum containing the supported combat spell type classifications.
 *
 * @author lare96
 */
public enum CombatSpellType {

    /**
     * Wind combat spell type.
     */
    WIND,

    /**
     * Water combat spell type.
     */
    WATER,

    /**
     * Fire combat spell type.
     */
    FIRE,

    /**
     * Earth combat spell type.
     */
    EARTH,

    /**
     * Smoke Ancient Magicks combat spell type.
     */
    SMOKE,

    /**
     * Shadow Ancient Magicks combat spell type.
     */
    SHADOW,

    /**
     * Blood Ancient Magicks combat spell type.
     */
    BLOOD,

    /**
     * Ice Ancient Magicks combat spell type.
     */
    ICE,

    /**
     * God spell combat type.
     */
    GOD,

    /**
     * Curse spell combat type.
     */
    CURSE,

    /**
     * Fallback combat spell type for spells that do not belong to another specialized classification.
     */
    OTHER
}